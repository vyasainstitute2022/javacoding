package com.vyasa.java.conditionalstatements;

public class SwitchExample {

	public static void main(String[] args) {

		int i=3;
		
		switch(2+1-1)
		{
		case 1: 
		      System.out.println("executed case 1");
		      break;
		
		case 2:
			 System.out.println("executed case 2");
			 break;
		case 3:
			 System.out.println("executed case 3");
			 break;
		case 4:
			 System.out.println("executed case 4");
			 break;
			
		default :
			 System.out.println("executed case default");
			 break;
		}
		
		
		
	}

}
