package com.vyasa.java.conditionalstatements;

public class WhileLoopExample {

	public static void main(String[] args) {

		int i=1;
		
	
		while(i<=10){
			
	     if(i%2==0) { 
              System.out.println("even number is:"+i); 
              }
            else{ 
             	System.out.println("odd number is :"+i); 
            } 
       i++;
	}
	}

}

