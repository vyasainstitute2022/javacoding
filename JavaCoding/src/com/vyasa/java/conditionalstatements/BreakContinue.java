public class BreakContinue {

	public static void main(String[] args) {
		

    	for (int i = 1; i <= 10; i++) {
                	System.out.println(i);
                		if (i==5) //5==5
                         continue;
                		System.out.println("i am inside loop");
                		
                		
                		
    	}
    	
    	
    	System.out.println("i am outside loop");
    	
}
}
