package com.vyasa.java.operators;

public class OperatorsUnary {
      
	public static void main(String[] args) {
		
		int x=50,y=-35;
		System.out.println("unary minus value of x :"+-x);
		System.out.println("unary plus value of y :"+-y);
		// +  +  ====>  +
		// -  -===> +
		//  - + ===>-
		//+  -===-
	}

}
